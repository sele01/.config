-------------------------
----- WINDOW RULES ------
-------------------------
hl.window_rule({
    name = "suppress-maximize",
    match = { class = ".*" },
    suppress_event = "maximize",
})

hl.window_rule({
    name = "ignore-phantom-windows",
    match = {
        class = "^$",
        title = "^$",
        xwayland = true,
        float = true,
        fullscreen = false,
        pin = false,
    },
    no_focus = true,
})

hl.window_rule({
    name = "test-browser",
    match = { class = "^" .. test_brow .. "$" },
    workspace = test_ws,
})

hl.window_rule({
    name = "browser",
    match = { class = "^" .. brow .. "$" },
    workspace = brow_ws,
})

hl.window_rule({
    name = "narrow-windows",
    match = { class = ".*" },
    center = true,
    pseudo = true,
})

hl.window_rule({ name = "popups", match = { workspace = "name:special:read" } })
hl.window_rule({ name = "popups", match = { workspace = "name:special:notes" } })

