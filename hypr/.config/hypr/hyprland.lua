hl.bind(mainMod .. "+Space", hl.dsp.window.cycle_next())
hl.bind(mainMod .. "+C", hl.dsp.window.close())
hl.bind(mainMod .. "+SHIFT+M", hl.dsp.exit())
hl.bind(mainMod .. "+W", hl.dsp.exec_cmd("makoctl dismiss"))

-- Mouse actions
